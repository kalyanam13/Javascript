// Array Methods

let num = [1 , 2 , 3 , 34 , 5];
let b = num.toString();
console.log(b , typeof b);

let c = num.join("_");
console.log(c);

let d = num.pop();
console.log(num , d);

let e = num.push(56);
console.log(num , e);

let f = num.shift();
console.log(num , f);

let g = num.unshift(45);
console.log(num , g);


